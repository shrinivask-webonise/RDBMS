CREATE TABLE dept(
     id INT PRIMARY KEY,
     name VARCHAR(30) NOT NULL,
     budget DOUBLE(10,3),
     manager_id INT,
     FOREIGN KEY (manager_id) REFERENCES emp(id) ON DELETE CASCADE ON UPDATE CASCADE
);
	
